const RedoLipRowTile = ({ tileName, comments, nameCode, pageNumber }) => {
  const queryClient = useQueryClient();
  const [emittedData, setemittedData] = useState("");
  const [open, setOpen] = useState(false);
  const [newNameCode, setNewNameCode] = useState("");
  const [inputValue, setInputValue] = useState("");

  // New auto states
  const [value, setValue] = useState([]);

  const { sendMessage, lastMessage, readyState } = useWebSocket(socketUrl, {
    onMessage: (message) => {
      const data = JSON.parse(message?.data);
      setemittedData(JSON.parse(data?.data));
    },
  });

  // const memoizedOptions = namecodes;

  const handleChange = (event, newValue) => {
    setValue([...newValue]);
    console.log(newValue)
  };

  const handleKeyDown = (event) => {
    const inputValue = event.target.value.trim();
    if (
      event.key === "Enter" &&
      inputValue.length > 0 &&
      /^\S+$/.test(inputValue)
    ) {
      setValue((prevValue) => [...prevValue, inputValue]);
      event.target.value = "";
      console.log('enter')
    }
  };

  const handleClickAndSendMessage = useCallback(
    (payload) =>
      sendMessage(
        JSON.stringify({
          user: username,
          ...payload,
        })
      ),
    [username]
  );
  const handelClick = () => {
    setOpen(open);
  };

  let UpdateRedoLipSync = async (videoId) => {
    try {
      fetch(`${BASE_URL}/audio/updt-redo-lip-newnamecode`, {
        method: "PUT",
        body: JSON.stringify({
          newNameCode: value.join(" ").trim(),
          videoId: videoId,
        }),
        headers: {
          "Content-type": "application/json; charset=UTF-8",
          Authorization: `Bearer ${accessToken}`,
        },
      })
        .then(() => {
          queryClient.invalidateQueries(["FetchAudioRedoLipSync", pageNumber]);
        })
        .then((data) => {});
    } catch (error) {
      console.log("Error occured", error);
    }
  };

  return (
    <div className="tile auto-complete">
      <div className="main-tile">
        <ColorCheckboxes
          tileName={tileName}
          handleClickAndSendMessage={handleClickAndSendMessage}
        />
        <div className="main-tile-head">
          <Typography
            className="video-name"
            sx={{
              paddingLeft: "1rem",
            }}
          >
            {tileName}
          </Typography>
          {emittedData &&
            JSON.parse(emittedData)?.filter(
              (data) => data?.video_id === tileName
            )?.length > 0 && (
              <Chip
                label={`In progress: ${
                  JSON.parse(emittedData)?.filter(
                    (data) => data?.video_id === tileName
                  )?.[0]?.user
                }`}
                sx={{ ml: "15px", backgroundColor: "#bcddfe", height:'unset',padding:'1px', color:'#1976d2', border:'1px solid #1976d2' }}
                ></Chip>
            )}
        </div>
        <p className="video-name-dynamic">{comments}</p>
      </div>
      <div className="main-tiles">
        <RedoLipModal
          onClick={handelClick}
          open={open}
          setOpen={setOpen}
          item={tileName}
        />
        <Box
          component="form"
          sx={{
            "& > :not(style)": { m: 1, width: "25ch" },
          }}
          noValidate
          autoComplete="off"
        >
          <TextField
            value={nameCode}
            id="outlined-basic"
            disabled
            wrap="hard"
            variant="outlined"
          />
          
          <Autocomplete
            multiple
            // id="fixed-tags-demo"
            value={value}
            onChange={handleChange}
            onKeyDown={handleKeyDown}
            options={namecodes}
            filterOptions={(options, {inputValue}) =>
              options.filter((option) =>
                option.toLowerCase().startsWith(inputValue.toLowerCase())
              )
            }
            isOptionEqualToValue={() => false}
            getOptionLabel={(option) => option}
            renderTags={(tagValue, getTagProps) =>
              tagValue.map((option, index) => (
                <Chip
                  key={index}
                  label={option}
                  style={{ background: "transparent" }}
                />
              ))
            }
            style={{ width: 500,transition:'unset' }}
            renderInput={(params) => (
              <TextField
                {...params}
                label="Namecode"
                placeholder="Type & Enter To Add New Value"
              />
            )}
          />
        </Box>
        <button
          onClick={() => {
            UpdateRedoLipSync(tileName.split("_")[3].split(".")[0]);
          }}
          className="primary-btn"
        >
          Change Now
        </button>
      </div>
    </div>
  );
};

